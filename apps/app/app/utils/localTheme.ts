export type LocalThemeColors = typeof LIGHT_COLORS

export const LOCAL_THEME = {
  colors: LIGHT_COLORS as LocalThemeColors,
  spacing: { xxxs: 2, xxs: 4, xs: 8, sm: 12, md: 16, lg: 24, xl: 32, xxl: 48, xxxl: 64 },
  typography: {
    primary: {
      thin: "System",
      light: "System",
      normal: "System",
      medium: "System",
      semiBold: "System",
      bold: "System",
    },
  },
  timing: { quick: 300 },
  isDark: false,
}

export function applyColorMode(isDark: boolean) {
  LOCAL_THEME.colors = isDark ? DARK_COLORS : LIGHT_COLORS
  LOCAL_THEME.isDark = isDark
}

export const themed: any = (styleOrFn: any) =>
  typeof styleOrFn === "function" ? styleOrFn(LOCAL_THEME) : styleOrFn

export type ThemedStyle<T> = (theme: typeof LOCAL_THEME) => T
export type ThemedStyleArray<T> = Array<T | ThemedStyle<T>>
